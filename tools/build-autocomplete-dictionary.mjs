#!/usr/bin/env node

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const DEFAULT_INPUT = "apps/extension/data/russian-autocomplete.seed.tsv";
const DEFAULT_OUTPUT = "apps/extension/src/content/autocomplete-dictionary.generated.js";
const DEFAULT_LIMIT = 50000;
const CYRILLIC_WORD_RE = /^[а-яё-]{2,32}$/iu;
const CYRILLIC_RE = /[а-яё]/iu;

const args = parseArgs(process.argv.slice(2));
const inputPath = args.input ?? DEFAULT_INPUT;
const outputPath = args.output ?? DEFAULT_OUTPUT;
const limit = Number.parseInt(args.limit ?? String(DEFAULT_LIMIT), 10);
const sourceLabel = args["source-label"] ?? path.relative(process.cwd(), inputPath);

if (!Number.isFinite(limit) || limit <= 0) {
  throw new Error("--limit must be a positive number");
}

const source = await readFile(inputPath, "utf8");
const entries = parseEntries(source)
  .filter((entry) => isAutocompleteWord(entry.word))
  .sort((left, right) => {
    const frequencyDelta = right.frequency - left.frequency;
    return frequencyDelta !== 0 ? frequencyDelta : left.word.localeCompare(right.word, "ru");
  })
  .slice(0, limit);

const generated = [
  "/*",
  " * Generated by tools/build-autocomplete-dictionary.mjs.",
  ` * Source: ${sourceLabel}.`,
  " * Keep source/license notes in docs before publishing.",
  " */",
  "window.RusTypeAutocompleteDictionary = [",
  ...entries.map((entry) => `  [${JSON.stringify(entry.word)}, ${entry.frequency}],`),
  "];",
  ""
].join("\n");

await writeFile(outputPath, generated, "utf8");

console.log(`Generated ${entries.length} autocomplete entries -> ${path.relative(process.cwd(), outputPath)}`);

function parseArgs(values) {
  const result = {};

  for (let index = 0; index < values.length; index += 1) {
    const value = values[index];

    if (!value.startsWith("--")) {
      continue;
    }

    const key = value.slice(2);
    const nextValue = values[index + 1];

    if (!nextValue || nextValue.startsWith("--")) {
      result[key] = "true";
      continue;
    }

    result[key] = nextValue;
    index += 1;
  }

  return result;
}

function parseEntries(source) {
  const byWord = new Map();

  source
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#"))
    .forEach((line, index) => {
      const entry = parseLine(line, index);

      if (!entry) {
        return;
      }

      const existing = byWord.get(entry.word);

      if (!existing || entry.frequency > existing.frequency) {
        byWord.set(entry.word, entry);
      }
    });

  return Array.from(byWord.values());
}

function parseLine(line, index) {
  const tabParts = line.split("\t").map((part) => part.trim()).filter(Boolean);
  const parts = tabParts.length >= 2
    ? tabParts
    : line.split(/[,\s]+/).map((part) => part.trim()).filter(Boolean);

  if (!parts.length) {
    return null;
  }

  const firstNumber = Number.parseFloat(parts[0]);
  const secondNumber = Number.parseFloat(parts[1]);
  const word = Number.isFinite(firstNumber) ? parts[1] : parts[0];
  const rawFrequency = Number.isFinite(firstNumber)
    ? firstNumber
    : Number.isFinite(secondNumber)
      ? secondNumber
      : DEFAULT_LIMIT - index;

  return {
    word: normalizeWord(word),
    frequency: normalizeFrequency(rawFrequency)
  };
}

function normalizeWord(word) {
  return String(word ?? "")
    .trim()
    .toLocaleLowerCase("ru-RU")
    .replace(/^[^а-яё]+|[^а-яё]+$/giu, "");
}

function normalizeFrequency(value) {
  const number = Number.parseFloat(value);

  if (!Number.isFinite(number)) {
    return 1;
  }

  return Math.max(1, Math.round(number));
}

function isAutocompleteWord(word) {
  return CYRILLIC_RE.test(word) &&
    CYRILLIC_WORD_RE.test(word) &&
    !word.startsWith("-") &&
    !word.endsWith("-") &&
    !word.includes("--");
}
